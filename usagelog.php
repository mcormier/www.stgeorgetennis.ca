          <center><b><font size="5">St. George's Tennis Club - Court Usage Log Summary</font></b></center>

During the last two seasons, St. George's staff have been tracking court usage. 
In addition to using this information for planning purposes, it is also being 
posted to let players know when the courts are likely to be busy. Evening and 
weekend information has been gathered from June to September, while only July 
and August (when school is out) has been used for weekday, daytime hours. The 
first two columns for each day represent the minimum number of courts in use for 
that day and time, the maximum number (with the "+ number" representing the 
number of courts worth of people waiting). The average column is the average 
court usage (courts in use with number of courts waiting). The colour coding is 
somewhat arbitrary with green being less than 3.5 courts in use on average, blue 
(it was hard to read the numbers using red) being 4.5 courts in use on average, 
and yellow in between.<br>
Some caveats:<br>
<b>></b> Some of the busiest times are related to our programs (wee-weekly, Sunday 
socials, Wednesday mixed, lessons)<br>
<b>></b> These are averages based on past history only and may change<br>
<b>></b> A review of the data showed that average court usage will increase during nice 
days in the middle of a stretch of bad weather, and may decrease during a 
stretch of good weather.<br>
