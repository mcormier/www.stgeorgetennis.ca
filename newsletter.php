<p>Dear members of St. George's,<br>
<br>
As we pass the coldest days of winter and see daylight hours lengthening, your 
Executive has started planning for the upcoming season. As a non-profit club, 
St. George's relies on a wide variety of volunteers for both general operation 
as well as to put on various programs and events during the season. Volunteering 
not only helps keep St. George's functioning at a reasonable cost to members, 
but also provides an opportunity for volunteers to meet and interact with a 
wider range of members. A description of the areas where volunteers are most in 
need is given below..<br>
<br>
1. Our Grounds Committee looks after the general maintenance and operations of 
the club, including the preparation and maintenance of the courts, the 
maintenance of the grounds and clubhouse. This committee could use some people 
interested in maintenance, repair and upgrades of the courts. While this winter 
has been mild, the lack of snow cover, and regular freeze thaw cycles plays 
havoc with the courts. Once the courts dry out sufficiently to begin work, a 
good group of volunteers can help accelerate the opening of the courts.<br>
<br>
2. Our Player Development/Junior Committee develops and operates our lesson 
sessions and associated programs (i.e. team tennis) and is interested in 
volunteers to help plan our player development program. Parents of juniors are 
especially welcome to help with our junior program. <br>
<br>
3. Our Tournament Committee hosts the St. George's Open and the St. George's 
Masters tournament under Tennis Nova Scotia as well as our annual club 
tournament. Each of these tournaments involve some pre-planning and enough 
volunteers to cover off the full week-end schedules of these tournaments.<br>
<br>
4. Our Public Relations/Marketing Committee is seeking ways to market the club 
and its programs both to attract new members and help existing members better 
understand and utilize the clubs services and is interested in receiving ideas 
and hearing about PR opportunities from members.<br>
<br>
5. The Executive is interested in finding ways to ease the integration of new 
and recent members into the club. While this initiative will cross over into the 
work of other committees (player development, social, PR/marketing) we are 
interested in support in two areas:<br>
- More experienced members who would be interested in occasionally serving as 
hitting partners/mentors for newer members who are learning the game. These 
volunteers do not need to be top players, but should be able to maintain control 
of the ball.<br>
- Recent members who would like to provide ideas on the types of orientation or 
events that would have made their initial introduction to both the club and the 
game of tennis a little easier (this could be done simply through e-mail 
exchanges, or if desired, a specific meeting)<br>
<br>
Please respond if you are interested in any of these and someone will get back 
to you with more details shortly. Members who have already identified an 
interest in volunteering may have already been contacted by a Committee member.<br>
<br>
Sincerely,<br>
Michael Hingston<br>
President, St. George's Tennis Club</p>

