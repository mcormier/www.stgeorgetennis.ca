<P>&nbsp;&nbsp;&nbsp;&nbsp; The St. George's Lawn Tennis Club of Dartmouth Nova
Scotia, was founded on St. George's Day, April 23. 1885. Tea parties, balls and garden parties
abounded in the early days of it's existence. The origin of lawn tennis, known variously as
<I>indoor</I>, <I>court</I>, and <I>royal tennis</I>, was a handball game played on indoor courts
in the thirteenth century France. Wooden paddles or bats were eventually replaced with rackets in
the sixteenth century and the indoor court game became what we would likely recognize as&nbsp; a
form of&nbsp; tennis. An outdoor version of tennis may have been seen in England as early as
1859, when Harry Gem and J.P. Perera set up and played on a grass court. Credit for popularizing
the game goes to a chap from Wales named Major Walter Clopton Wingfield.</P>

<P>&nbsp;&nbsp;&nbsp;&nbsp; Prior to tennis, the stately game of croquet&nbsp; was played primarily
by wealthy individuals, but due to Wingfield's marketing efforts and for a more active outdoor
sport, tennis quickly replaced croquet.</P>

<P>Lawn tennis is thought to have made it's first appearance in Canada in the Toronto area with the
Toronto Tennis Club being formed in either 1875 or 1876. The St. George's Club after it's
founding in 1885 is similar to all successful organizations and it comes with it's own unique
stories of people and events, aspirations and achievements, which you can read about in a book
written in 1985 as a commemorative book to help celebrate St. George's Hundredth Anniversary.</P>

<P>&nbsp;&nbsp;&nbsp;&nbsp; The first days of tennis were played on grass. Keeping five grass courts
in playing shape today would be a matter of a few hours atop a modern-day riding mower.&nbsp; St.
George's&nbsp; Lawn courts ,at the turn of the century, were kept trim by use of&nbsp; horses
fitted with leather boots so that their hooves did not cut into the turf while pulling a mower
over the surface.</P>

