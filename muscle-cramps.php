        <p align="justify">&nbsp;&nbsp; It is usually not necessary to see a doctor about your muscle cramps. Most likely, they will go away on their own, but there are several treatments you can try to help them along. First, stop doing the activity that caused the muscle cramp. Next, stretch out the cramped muscle and massage it until the cramp ceases. If your cramped muscle is sore or tender, apply cold; if it is tense or tight, apply heat. Warm up and stretch before exercising, and work toward better overall fitness.</p>
        <p align="justify">&nbsp;&nbsp; There are also several treatment options for treating more specific muscle cramps. Every muscle in the body has an antagonist, so the quickest way to relieve a cramping muscle is to put antagonistic pressure on the involved muscles. This pressure overwhelms the muscle cramp and causes a release, which relieves the pain of the cramp.</p>
        <p align="justify"><b>Treating a Cramp in the Front of Your Thigh (Quadriceps)</b><br>
          &nbsp;&nbsp;
          If the cramp occurs in the front of your right thigh, sit down, and place your left ankle behind your right ankle. Then, pull back with as much force as you can with your right ankle, while applying pressure to your left ankle. Contracting your right thigh muscle will relieve the cramp. If the cramp is in your left thigh, reverse the process.
        <br>
        <br>
          <b>Treating a Cramp in the Back of Your Thigh (Hamstring)</b><br>
          &nbsp;&nbsp;
          If the cramp occurs in the back of your right thigh, place your right ankle behind your left ankle and push hard with your right ankle, using your left leg as an anchor. This action causes your hamstring to contract and relieves the cramp. If the cramp occurs in your left hamstring, reverse the process.
        <br>
        <br>
          <b>Treating a Cramp in Your Calve. (Gastrocnemious)</b><br>
          &nbsp;&nbsp;
          If the cramp occurs in your right calve, sit or lie down. Then, place your left foot over your right instep and pull your right foot towards you. Push with your left foot with as much force as you can. This will cause your lower leg muscles to contract and will relieve the cramp.<br>
&nbsp;<br>
          <b>Treating Abdominal Muscle Spasms and Spasms along the Rib Cage <br>
        (&rdquo;Stitch in the Side&rdquo;)</b><br>
          &nbsp;&nbsp;
          Abdominal cramps and &ldquo;stitch in the side&rdquo; can be caused by exercising, stretching, or even deep breathing. Because there are at least 13 muscle groups in this area, it is very difficult to locate the muscle that is cramping. For this reason, it is almost impossible to try and apply antagonistic pressure to release the cramp. However, there are other ways to try and alleviate the pain. Lie on your back and put both of your arms under your head, grasping both elbows. Raise your head as high as you can to support it, then inhale deeply and count to 8. Exhale and count to 8. Repeat this process until you experience relief. If you are experiencing &ldquo;stitch in the side&rdquo; on your right side, repeat the above treatment while pulling the right elbow and your head as far to the left as your can. If you have &ldquo;stitch in the side&rdquo; on your left side, reverse the process.<br>
&nbsp;<br>
          <b>Treating a Muscle Spasm in Your Back</b><br>
          &nbsp;&nbsp;
          There are two different stretching exercises you can do to relieve the pain of a muscle spasm in your back. The first one is the knee to chest stretch. This stretch should be done following an ice massage. Lie on your back and pull one or both of your knees up to your chest. Remain in this position for one minute.</p>
        <p align="justify">&nbsp;&nbsp; The second exercise is the back stretch. Get on your hands and knees and arch your back. Bring it down and back up 5-10 times. This exercise should be done regularly throughout the day. </p>
        <p align="justify">&nbsp;&nbsp; Although most muscle cramps are nothing to be concerned about, others can be the result of a serious medical condition. If your cramps are severe, happen frequently, do not respond to treatment, of are not related to strenuous exercise, you should see your doctor.</p>
