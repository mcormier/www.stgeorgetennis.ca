          are revealed to every recipient.</font></li>
          <li><font color="#FF0000">The<tt> BCC: </tt>header is not.</font></li>
          <li><font color="#FF0000">Don't put multiple recipients in<tt> CC:
          </tt>unless you intend them to see each others' addresses.</font></li>
          <li><font color="#FF0000">Leaking email address lists via<tt> CC: </tt>
          helps spammers and scammers, even if only slightly.</font></li>
